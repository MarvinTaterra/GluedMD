def test_switching_value(platform):
    """Switching function s(RMSD/r0) matches Python for a controllable RMSD."""
    # Scaled template: positions = scale * template (already centered → COM=0)
    # d_k = (scale-1)*t_k → MSD = (scale-1)^2 * (1/30)*Σ|t_k|^2
    r0 = 0.5
    scale = 0.7

    tmpl_msd = sum(sum(v**2 for v in t) for t in ALPHA_TEMPLATE) / 30.0
    expected_rmsd = abs(scale - 1.0) * math.sqrt(tmpl_msd)
    expected_s = _switching(expected_rmsd, r0)

    positions = [[ALPHA_TEMPLATE[i][k] * scale for k in range(3)] for i in range(30)]
    positions += [[0.0]*3]

    ctx, f = _make_ctx(positions, list(range(30)), 0, r0, platform)
    cvs = f.getLastCVValues(ctx)
    assert abs(cvs[0] - expected_s) < TOL_CV, \
        f"switching: expected {expected_s:.6f}, got {cvs[0]:.6f}  (rmsd={expected_rmsd:.4f})"
    print(f"  test_switching_value: OK  (cv={cvs[0]:.6f}, ref={expected_s:.6f})")


def test_force_direction(platform):
    """Force on atom k = jacFactor * dk (sign checked) for single alpha window."""
    rng = random.Random(99)
    N = 30
    r0 = 0.5
    noise = 0.05
    positions = [[ALPHA_TEMPLATE[k][c] + rng.uniform(-noise, noise)
                  for c in range(3)] for k in range(N)]
    positions += [[0.0]*3]
    atom_list = list(range(N))
    template = ALPHA_TEMPLATE

    ctx, f = _make_ctx(positions, atom_list, 0, r0, platform, bias_grad=1.0)
    state = ctx.getState(getForces=True)
    unit = mm.unit.kilojoules_per_mole / mm.unit.nanometer
    raw = state.getForces(asNumpy=False)

    # Python reference Jacobian
    pts = [positions[k] for k in range(N)]
    cx = sum(p[0] for p in pts) / N
    cy = sum(p[1] for p in pts) / N
    cz = sum(p[2] for p in pts) / N
    dx = [pts[k][0] - cx - template[k][0] for k in range(N)]
    dy = [pts[k][1] - cy - template[k][1] for k in range(N)]
    dz = [pts[k][2] - cz - template[k][2] for k in range(N)]
    msd = sum(dx[k]**2 + dy[k]**2 + dz[k]**2 for k in range(N)) / N
    rmsd = math.sqrt(msd)

    if rmsd < 1e-6 or rmsd / r0 >= 0.9999:
        jf = 0.0
    else:
        x = rmsd / r0
        x5 = x**5; x6 = x**6; x12 = x**12
        num = 1.0 - x6; den = 1.0 - x12
        dsdx = -6.0 * x5 * num**2 / den**2
        jf = dsdx / (r0 * N * rmsd)  # jacFactor denominator = N=30

    for k in range(N):
        got_x = raw[k][0].value_in_unit(unit)
        got_y = raw[k][1].value_in_unit(unit)
        got_z = raw[k][2].value_in_unit(unit)
        assert abs(got_x - (-jf * dx[k])) < TOL_F, \
            f"atom {k} x: expected {-jf*dx[k]:.4f}, got {got_x:.4f}"
        assert abs(got_y - (-jf * dy[k])) < TOL_F, \
            f"atom {k} y: expected {-jf*dy[k]:.4f}, got {got_y:.4f}"
        assert abs(got_z - (-jf * dz[k])) < TOL_F, \
            f"atom {k} z: expected {-jf*dz[k]:.4f}, got {got_z:.4f}"
    print(f"  test_force_direction: OK  (rmsd={rmsd:.4f}, jf={jf:.6f})")


def test_numerical_gradient(platform):
    """Finite-difference check of Jacobian for alpha helix (2 windows)."""
    rng = random.Random(55)
    # 7 residues = 35 atoms → 2 windows
    N_res = 7
    N = N_res * 5
    r0 = 0.5
    noise = 0.05
    positions = [[ALPHA_TEMPLATE[i % 30][c] + rng.uniform(-noise, noise)
                  for c in range(3)] for i in range(N)]
    positions += [[0.0]*3]
    atom_list = list(range(N))

    sys_obj = mm.System()
    for _ in range(N + 1):
        sys_obj.addParticle(12.0)
    f_cv = gp.GluedForce()
    f_cv.setUsesPeriodicBoundaryConditions(False)
    av = mm.vectori()
    for a in atom_list:
        av.append(a)
    f_cv.addCollectiveVariable(gp.GluedForce.CV_SECONDARY_STRUCTURE, av,
                               mm.vectord([0.0, r0]))
    f_cv.setTestBiasGradients(mm.vectord([1.0]))
    sys_obj.addForce(f_cv)
    ctx = mm.Context(sys_obj, mm.VerletIntegrator(0.001), platform)

    h = 1e-4
    max_err = 0.0
    unit = mm.unit.kilojoules_per_mole / mm.unit.nanometer

    for ai in range(N):
        for ci in range(3):
            pos_p = [list(p) for p in positions]
            pos_m = [list(p) for p in positions]
            pos_p[ai][ci] += h
            pos_m[ai][ci] -= h
            fd = (_compute_cv_reference(pos_p, atom_list, 0, r0) -
                  _compute_cv_reference(pos_m, atom_list, 0, r0)) / (2*h)

            ctx.setPositions(positions)
            state = ctx.getState(getForces=True)
            raw = state.getForces(asNumpy=False)
            f_anal = -raw[ai][ci].value_in_unit(unit)
            max_err = max(max_err, abs(fd - f_anal))

    assert max_err < TOL_F, f"max gradient error {max_err:.2e} > {TOL_F}"
    print(f"  test_numerical_gradient: OK  (max_err={max_err:.2e})")


def test_two_secondary_structure_cvs(platform):
    """Two independent SS CVs (alpha + antibeta) on the same system."""
    rng = random.Random(17)
    r0 = 0.1
    N = 30

    pos_alpha = [[rng.uniform(-0.3, 0.3) for _ in range(3)] for _ in range(N)]
    pos_beta  = [[rng.uniform(-0.3, 0.3) for _ in range(3)] for _ in range(N)]
    positions = pos_alpha + pos_beta + [[0.0]*3]
    atoms_alpha = list(range(N))
    atoms_beta  = list(range(N, 2*N))

    n_atoms = len(positions)
    sys = mm.System()
    for _ in range(n_atoms):
        sys.addParticle(12.0)
    force = gp.GluedForce()
    force.setUsesPeriodicBoundaryConditions(False)

    av0 = mm.vectori()
    for a in atoms_alpha: av0.append(a)
    av1 = mm.vectori()
    for a in atoms_beta: av1.append(a)
    force.addCollectiveVariable(gp.GluedForce.CV_SECONDARY_STRUCTURE, av0,
                                mm.vectord([0.0, r0]))
    force.addCollectiveVariable(gp.GluedForce.CV_SECONDARY_STRUCTURE, av1,
                                mm.vectord([1.0, r0]))
    force.setTestBiasGradients(mm.vectord([0.0, 0.0]))
    sys.addForce(force)
    ctx = mm.Context(sys, mm.VerletIntegrator(0.001), platform)
    ctx.setPositions(positions)
    ctx.getState(getForces=True)

    cvs = force.getLastCVValues(ctx)
    ref0 = _compute_cv_reference(positions, atoms_alpha, 0, r0)
    ref1 = _compute_cv_reference(positions, atoms_beta, 1, r0)
    assert abs(cvs[0] - ref0) < TOL_CV, f"CV0 mismatch: {cvs[0]:.6f} vs {ref0:.6f}"
    assert abs(cvs[1] - ref1) < TOL_CV, f"CV1 mismatch: {cvs[1]:.6f} vs {ref1:.6f}"
    print(f"  test_two_secondary_structure_cvs: OK  (cv0={cvs[0]:.4f}, cv1={cvs[1]:.4f})")


