def _switching(rmsd, r0):
    if rmsd < 1e-6:
        return 1.0
    x = rmsd / r0
    if x >= 0.9999:
        return 0.0
    x6 = x**6
    return (1.0 - x6) / (1.0 - x6 * x6)


def _window_rmsd(positions_win, template):
    """Translation-only RMSD: center both, then sqrt(mean |d|^2)."""
    n = len(template)
    cx = sum(positions_win[k][0] for k in range(n)) / n
    cy = sum(positions_win[k][1] for k in range(n)) / n
    cz = sum(positions_win[k][2] for k in range(n)) / n
    msd = 0.0
    for k in range(n):
        dx = positions_win[k][0] - cx - template[k][0]
        dy = positions_win[k][1] - cy - template[k][1]
        dz = positions_win[k][2] - cz - template[k][2]
        msd += dx*dx + dy*dy + dz*dz
    return math.sqrt(msd / n)


def _compute_cv_reference(positions, atoms, subtype, r0):
    """Python reference for one CV value (30-atom backbone windows)."""
    N = len(atoms)
    stride = 5 if subtype == 0 else 30
    num_windows = (N // 5 - 5) if subtype == 0 else (N // 30)
    templates = TEMPLATES_BY_SUBTYPE[subtype]
    cv = 0.0
    for w in range(num_windows):
        wStart = w * stride
        pts = [positions[atoms[wStart + k]] for k in range(30)]
        if subtype == 2:
            rmsd1 = _window_rmsd(pts, templates[0])
            rmsd2 = _window_rmsd(pts, templates[1])
            rmsd = min(rmsd1, rmsd2)
        else:
            rmsd = _window_rmsd(pts, templates[0])
        cv += _switching(rmsd, r0)
    return cv


def _make_ctx(positions, atom_list, subtype, r0, platform, bias_grad=0.0):
    n_atoms = len(positions)
    sys = mm.System()
    for _ in range(n_atoms):
        sys.addParticle(12.0)
    f = gp.GluedForce()
    f.setUsesPeriodicBoundaryConditions(False)
    av = mm.vectori()
    for a in atom_list:
        av.append(a)
    f.addCollectiveVariable(gp.GluedForce.CV_SECONDARY_STRUCTURE, av,
                            mm.vectord([float(subtype), r0]))
    f.setTestBiasGradients(mm.vectord([bias_grad]))
    sys.addForce(f)
    ctx = mm.Context(sys, mm.VerletIntegrator(0.001), platform)
    ctx.setPositions(positions)
    ctx.getState(getForces=True)
    return ctx, f


def test_perfect_alpha_single_window(platform):
    """CV=1 when 30 atoms exactly match centered alpha template (any translation)."""
    offset = [0.5, 0.3, 0.2]
    positions = [[t[k] + offset[k] for k in range(3)] for t in ALPHA_TEMPLATE]
    positions += [[0.0]*3]  # spare atom

    ctx, f = _make_ctx(positions, list(range(30)), 0, 0.08, platform)
    cvs = f.getLastCVValues(ctx)
    assert abs(cvs[0] - 1.0) < TOL_CV, \
        f"perfect alpha: expected CV=1.0, got {cvs[0]:.6f}"
    print(f"  test_perfect_alpha_single_window: OK  (cv={cvs[0]:.8f})")


def test_cv_value_alpha(platform):
    """CV matches Python reference for alpha subtype (2 windows = 40 backbone atoms)."""
    rng = random.Random(42)
    # 7 residues = 35 backbone atoms → numWindows = 35/5-5 = 7-5 = 2 windows
    N_res = 7
    N = N_res * 5  # 35 backbone atoms
    r0 = 0.08
    offset = [0.5, 0.5, 0.5]
    # Repeat the 30-atom template pattern, cycling through atoms
    positions = [[ALPHA_TEMPLATE[i % 30][k] + offset[k] + rng.uniform(-0.02, 0.02)
                  for k in range(3)] for i in range(N)]
    positions += [[0.0]*3]
    atom_list = list(range(N))

    ctx, f = _make_ctx(positions, atom_list, 0, r0, platform)
    cvs = f.getLastCVValues(ctx)
    ref = _compute_cv_reference(positions, atom_list, 0, r0)
    assert abs(cvs[0] - ref) < TOL_CV, \
        f"alpha cv: expected {ref:.6f}, got {cvs[0]:.6f}"
    print(f"  test_cv_value_alpha: OK  (cv={cvs[0]:.6f}, ref={ref:.6f})")


